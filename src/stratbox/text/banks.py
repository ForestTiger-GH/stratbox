"""
banks.py — утилиты для работы с названиями банков.

Важно:
- это место заранее зарезервировано под будущую нормализацию имени банка,
  чтобы в реестре появлялся дополнительный столбец "bank_name_norm".
- пока функция является заглушкой и возвращает исходное имя без изменений.
"""


def normalize_bank_name(name: str) -> str:
    """
    Возвращает упрощённое (нормализованное) имя банка.

    Сейчас: заглушка, возвращает исходное имя.
    Позже: сюда будет добавлена реальная логика нормализации (сокращения, чистка кавычек,
    удаление организационно-правовой формы и т.п.).
    """
    if name is None:
        return ""
    return str(name).strip()
